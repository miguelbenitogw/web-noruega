import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { pathToFileURL } from 'node:url';

function stableStringify(value) {
  if (value === null || typeof value !== 'object') {
    return JSON.stringify(value);
  }

  if (Array.isArray(value)) {
    return `[${value.map((item) => stableStringify(item)).join(',')}]`;
  }

  const entries = Object.keys(value)
    .sort()
    .map((key) => `${JSON.stringify(key)}:${stableStringify(value[key])}`);

  return `{${entries.join(',')}}`;
}

function escapeSqlString(value) {
  return value.replace(/'/g, "''");
}

async function main() {
  const repoRoot = process.cwd();
  const sourcePath = path.resolve(repoRoot, 'src/data/siteContent.js');
  const outputPath = path.resolve(repoRoot, 'docs/sql/content-backfill.sql');

  const module = await import(pathToFileURL(sourcePath).href);
  const siteContent = module.default;

  if (!siteContent || typeof siteContent !== 'object' || Array.isArray(siteContent)) {
    throw new Error('src/data/siteContent.js must default-export a plain object.');
  }

  const contentJson = stableStringify(siteContent);
  const escapedJson = escapeSqlString(contentJson);

  const sql = `-- Generated by scripts/generate-content-backfill-sql.mjs
-- Source: src/data/siteContent.js

insert into public.content_snapshots (locale, status, content, updated_at, published_at)
values
  ('nb', 'published', '${escapedJson}'::jsonb, now(), now()),
  ('nb', 'draft', '${escapedJson}'::jsonb, now(), null)
on conflict (locale, status) do update
set content = excluded.content,
    updated_at = now(),
    published_at = excluded.published_at;
`;

  await mkdir(path.dirname(outputPath), { recursive: true });
  await writeFile(outputPath, sql, 'utf8');

  console.log(`Wrote ${path.relative(repoRoot, outputPath)}`);
}

await main();
